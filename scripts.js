function filterByLetter(letter) {
  const filteredWomen = (letter === 'All')
    ? allBlackWomen
    : allBlackWomen.filter(woman => woman.name.startsWith(letter));
  renderBlackWomenCards(filteredWomen);
}

function renderBlackWomenCards(womenToDisplay) {
  const cardContainer = document.getElementById("black-women-container");
  cardContainer.innerHTML = "";
  const templateCard = document.querySelector(".card");

  womenToDisplay.forEach(woman => {
    const nextCard = templateCard.cloneNode(true);
    editBlackWomanCardContent(nextCard, woman);
    cardContainer.appendChild(nextCard);
  });
}

// Function to fill the card with a Black woman's information
function editBlackWomanCardContent(card, woman) {
  card.style.display = "block"; // Make the card visible

  // Here we create the first image element
  const cardImage1 = document.createElement("img");
  cardImage1.src = woman.imageURL1;
  cardImage1.alt = woman.name;

  // For second image element
  const cardImage2 = document.createElement("img");
  cardImage2.src = woman.imageURL2;
  cardImage2.alt = woman.name;

  const cardHeader = card.querySelector("h2"); // Get the heading element in the card
  cardHeader.textContent = woman.name; // To set the woman's name as the heading

  const cardDescription = card.querySelector(".card-content ul"); // Get the list for the description
  cardDescription.innerHTML = ""; // Clear any existing description items

  const descriptionPoints = woman.description.split(". "); // Split the description into sentences
  descriptionPoints.forEach(sentence => {
    const listItem = document.createElement("li"); // Create a list item for each sentence
    listItem.textContent = sentence + "."; // Set the sentence as the list item text
    cardDescription.appendChild(listItem); // Add the list item to the description
  });

  const cardContentDiv = card.querySelector(".card-content");

  // Image container 
  const imageContainer = document.createElement("div");
  imageContainer.classList.add("image-container");

  // Append the images to the image container
  imageContainer.appendChild(cardImage1);
  imageContainer.appendChild(cardImage2);

  // Insert the image container before the heading in the card content
  cardContentDiv.insertBefore(imageContainer, cardHeader);

  console.log("new woman card:", woman.name, "- html: ", card); 
}

function showRandomWoman() {
  const randomIndex = Math.floor(Math.random() * blackWomen.length);
  const randomWoman = blackWomen[randomIndex];
  renderBlackWomenCards([randomWoman]); // Render only the randomly selected woman
  scrollToSection('explore-herstory-section'); // Optionally scroll to the catalog section
}

function scrollToSection(id) {
  const element = document.getElementById(id);
  element.scrollIntoView({ behavior: 'smooth' });
}

// To run the showBlackWomenCards function when the page is loaded
document.addEventListener("DOMContentLoaded", showBlackWomenCards);